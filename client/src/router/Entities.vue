<template>
  <div class="container-fluid">
        <notifications group="custom-template"
                   :duration="5000"
                   :width="500"
                   animation-name="v-fade-left"
                   position="top left">
    </notifications>
  <modal-info v-if="isModalInfoOpen" @close="isModalInfoOpen = false" :entity="modalEntity"></modal-info>
  <modal-delete v-if="isModalDeleteOpen" @close="isModalDeleteOpen = false" :entity="modalEntity"></modal-delete>

    <b-button variant="dark" v-if="currentUser.role === 'Admin'" @click.prevent="$router.push('/entity/new')">Créer une entité</b-button>    
    <br><br>
    <table class="table">
      <thead class="color">
        <tr>
          <th>Nom</th>
          <th>Type</th>
          <th>Détails</th>
          <th v-if="currentUser === 'Admin'">Editer</th>
          <th v-else></th>
          <th v-if="currentUser === 'Admin'">Supprimer</th>
          <th v-else></th>
        </tr>
      </thead>
      <tbody v-for='(entity, index) in entities' :key='entity._id'>
        <tr>
          <td> {{entity.name}} </td>
          <td> {{entity.typeEntity}} </td>
          <td>
            <b-button variant="info" @click="showModalInfo(entity)">Détails</b-button>
          </td>
          <td>
            <b-button v-if="currentUser.role === 'Admin'" type="button" 
                    @click.prevent='goToEdit(entity._id)'
                    variant="warning">Editer
                    </b-button>
          </td>
          <td>
            <b-button v-if="currentUser.role === 'Admin'" id="show-modal-delete" @click="showModalDelete(entity)" variant="danger">Supprimer</b-button>
          </td>
        </tr>
      </tbody>
    </table>
  </div>
</template>
<script>
import { getEntities, getSingleUser, checkUser } from "@/api/auth";
import ModalInfo from "@/components/ModalInfo";
import ModalDelete from "@/components/ModalDelete";
export default {
  components: {
    ModalInfo,
    ModalDelete
  },
  data() {
    return {
      entities: [],
      modalEntity: null,
      currentUser: true,
      isModalInfoOpen: false,
      isModalDeleteOpen: false,
      animation: {
        enter: {
          opacity: [1, 0],
          translateX: [0, -300],
          scale: [1, 0.2]
        },
        leave: {
          opacity: 0,
          height: 0
        }
      }
    };
  },

  created() {
    checkUser(this.$root);
    if (!this.$root.user) this.$router.push("/404");
    getEntities().then(entities => {
      this.entities = entities;
    });
    getSingleUser(this.$root.user._id).then(user => {
      this.currentUser = user;
    });
  },

  methods: {
    goToEdit(_id) {
      this.$router.push("/entity/" + _id);
    },
    showModalInfo(entity) {
      this.modalEntity = entity;
      this.isModalInfoOpen = true;
    },
    showModalDelete(entity) {
      this.modalEntity = entity;
      this.isModalDeleteOpen = true;
    }
  }
};
</script>

